'''Напишите функцию, которая на вход принимает единственное целое число N, а возвращает целый квадратный корень из этого
 числа, если такой существует, или None, если такого корня нет.
 Нельзя использовать функцию sqrt() из модуля math для извлечения квадратного корня.'''


def get_sqrt(N):
    sq = N ** 0.5
    if (sq % 1) != 0:
        return None
    else:
        return int(sq)


print(get_sqrt(100))

