'''Напишите функцию, которая принимает на вход строку, состояющую из символов 
'(' и ')', задающих скобочную последовательность, и возвращает True, 
если последовательность корректна, иначе False.'''


def func(pos):
    count = 0
    for i in pos:
        if i == '(':
            count += 1
        elif i == ')':
            count -= 1
        if count == -1:
            return False
    if count == 0:
        return True
    else:
        return False


print(func('((()()()))'))

